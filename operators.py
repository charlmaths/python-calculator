# class operators

class Operators:
    '''Basic Operator Class'''
    def __init__(self):
        self.num1 = 0
        self.num2 = 0


    def add(self):
        return self.num1 + self.num2


    def subtract(self):
        return self.num1 - self.num2


    def multiply(self):
        return self.num1 * self.num2


    def divide(self):
        return self.num1 / self.num2


